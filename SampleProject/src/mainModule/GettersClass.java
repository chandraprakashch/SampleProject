package mainModule;

public class GettersClass {

	private int marks;
	private String str;
	private Float number;

	public int getMarks() {
		return marks;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}

	public String getStr() {
		return str;
		
	}

	public void setStr(String str) {
		this.str = str;
		
	}

	public Float getNumber() {
		return number;
		
	}

	public void setNumber(Float number) {
		this.number = number;
		
	}
	
}
